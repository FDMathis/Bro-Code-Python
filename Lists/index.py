# list = used to store multiple items in a single variable

food = ["pizza", "cheeseburger", "hotdog", "spaghetti", "pudding"]
print(food)

food[0] = "sushi"
print(food)

food.append("ice cream")
print(food)

food.remove("hotdog")
print(food)

food.pop()
print(food)

food.insert(0, "cake")
print(food)

food.sort()
print(food)

food.clear()
print(food)

